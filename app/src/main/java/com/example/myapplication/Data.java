package com.example.myapplication;
//연락처 데이터
public class Data {

    private String name;
    private String number;

    //연락처 이름 데이터 관련 함수
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    //연락처 번호 데이터 관련 함수
    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number =number;
    }

}
